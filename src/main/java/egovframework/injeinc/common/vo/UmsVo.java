package egovframework.injeinc.common.vo;

public class UmsVo {
	
	String sendType			= "";
	String userId			= "";
	String subject			= "";
	String address			= "";
	String content			= "";
	String callbackNo		= "";
	String sent				= "";
	String scheduleStime	= "";
	
	public String getSendType() {
		return sendType;
	}
	public void setSendType(String sendType) {
		this.sendType = sendType;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCallbackNo() {
		return callbackNo;
	}
	public void setCallbackNo(String callbackNo) {
		this.callbackNo = callbackNo;
	}
	public String getSent() {
		return sent;
	}
	public void setSent(String sent) {
		this.sent = sent;
	}
	public String getScheduleStime() {
		return scheduleStime;
	}
	public void setScheduleStime(String scheduleStime) {
		this.scheduleStime = scheduleStime;
	}
	
}